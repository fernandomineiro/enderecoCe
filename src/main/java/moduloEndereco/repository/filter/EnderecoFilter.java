package moduloEndereco.repository.filter;

public class EnderecoFilter {

	private Short cdLocalidade;
	private Short cdBairro;
	private Short cdLogradouro;

	public Short getCdLocalidade() {
		return cdLocalidade;
	}

	public void setCdLocalidade(Short cdLocalidade) {
		this.cdLocalidade = cdLocalidade;
	}

	public Short getCdBairro() {
		return cdBairro;
	}

	public void setCdBairro(Short cdBairro) {
		this.cdBairro = cdBairro;
	}

	public Short getCdLogradouro() {
		return cdLogradouro;
	}

	public void setCdLogradouro(Short cdLogradouro) {
		this.cdLogradouro = cdLogradouro;
	}

}
